<template>
  <button @click="isShow = !isShow">切換顯示/隱藏</button>
  <Demo v-if="isShow"></Demo>
</template>

<script>
  import { ref } from "vue";
  import Demo from "./components/Demo";

  export default {
    name: "App",
    components: { Demo },
    setup() {
      let isShow = ref(true);

      return { isShow };
    },
  };
</script>
